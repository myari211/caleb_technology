 }          
            
function doSave() { 
    var full_name = $("#full_name").val();
    var company =  $("#company").val();
    var email =  $("#email").val();
    var phone =  $("#phone").val();
    var message =  document.getElementById("message").value;
    if (full_name !== '' && company !== '' && email!=='' && phone!=='' && message!== ''){
        saveSubpage(full_name,company, email,phone,message);
        $('#contactForm').hide();
        $('#thankyou').show();
    }
    else {
        return false;
    }
}

function saveSubpage(full_name,company, email,phone,message)
{
    $.ajax({
            url: '<?php echo BASE_URL_BACKEND;?>/Contact/contactUs',
            type: 'post',
            data: {full_name: full_name, company: company, email: email, phone: phone,message: message},

            beforeSend: function()
            {	
            $('.loadergif').show();
            },
            complete: function()
            {
            $('#contactForm').hide();
            $('#thankyou').show();
            }
            });
}
</script>
